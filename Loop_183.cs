using System;





class Program
{
    static void Main()
    {
        // Continue in while-loop until index is equal to 10.
        int i = 0;
        while (i < 10)
        {
            Console.Write("While statement ");
            // Write the index to the screen.
            Console.WriteLine(i);
            // Increment the variable.
            i++;
        }
    }
}

